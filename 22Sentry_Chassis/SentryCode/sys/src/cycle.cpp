#include "cycle.h"

#define NOW 0
#define OLD 1
#define NEW 2


/**
  * @brief 資函扮寂佚連痕方
  * @return 寂侯議扮寂巓豚
  */
float Cycle::getCycleT()	
{
	cycleTime[OLD] = cycleTime[NOW];	//貧匯肝議扮寂
	cycleTime[NOW] = getSysTimeUs()/1000000.0f; //云肝議扮寂
	cycleTime[NEW] = (( cycleTime[NOW] - cycleTime[OLD] ) );//寂侯議扮寂巓豚
	//及匯肝塰佩卦指巣
	if(cycleTime[OLD]==0)
	{
		return 0;
	}
	return cycleTime[NEW];
}

